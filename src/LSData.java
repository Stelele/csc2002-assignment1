public class LSData 
{
    private String area;
    private String regions;
    
    public LSData(String area, String regions)
    {
        this.area = area;
        this.regions = regions;
    }
    
    public String getArea()
    {
        return area;
    }
    
    public String getRegions()
    {
        return regions;
    }
    
    public void setArea(String area)
    {
        this.area = area;
    }
    
    public void setRegions(String regions)
    {
        this.regions = regions;
    }
    
}